    username = update.effective_user.username or "Unknown"
    
    logger.info(f"[USER {user_id}] (@{username}) –ó–∞–ø—É—Å—Ç–∏–ª –±–æ—Ç–∞ –∫–æ–º–∞–Ω–¥–æ–π /start")
    
